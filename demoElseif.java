package day2;

public class demoElseif {

	public static void main(String[] args) {
	   
		
		int a=60;
		if (a>=90 & a<=100)
		{
			System.out.println("A+");
		}
		else if (a>=80 & a<=90)
		{
			System.out.println("A");
		}
		else if (a>=70 & a<=80)
		{
			System.out.println("B+");
		}
		else if (a<=70)
		{
			System.out.println("Fail");
		}
	}

}
